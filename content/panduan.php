<h3>Panduan Penggunaan</h3>

<div class="well"><span style="text-align: justify;">Dalam Portal Pengumuman Kelulusan Ujian Nasional ada beberapa data yang harus kamu gunakan untuk dapat mengakses pengumuman, yaitu :</span>
<div>
<ul><li>
<div style="text-align: justify;">
* <b>NO. PESERTA UN </b>&nbsp;: Nomor peserta yang tertera dalam kartu peserta UN, contoh : 09-006-001-9</div></li>
<li><div style="text-align: justify;">
* <b>PASSWORD &nbsp;: </b>Untuk password disini menggunakan tanggal kelahiran dengan format <u style="font-style: italic; font-weight: bold;">ddmmyyy</u>&nbsp;, jadi jika kamu lahir pada 10 April 1997, maka dalam isian password cukup mengetikkan <b><i><u>10041994</u></i></b></div></li></ul>
<div style="text-align: justify;">
Setelah memasukkan data diatas, selanjutnya yaitu memilih <u style="font-weight: bold;">Lihat Hasil Kelulusan</u>&nbsp;maka hasil kelulusan kamu akan segera keluar
</div>
</div>